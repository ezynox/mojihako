        function setActiveTab(tabId, shouldFocus = false) {
            if (!tabIds.includes(tabId)) return;
            activeTab = tabId;
            writeStoredString(`${storagePrefix}tab`, activeTab);
            tabButtons.forEach((button) => {
                const selected = button.dataset.kaomojiTab === activeTab;
                button.setAttribute("aria-selected", selected ? "true" : "false");
                button.tabIndex = selected ? 0 : -1;
                if (selected && shouldFocus) button.focus();
            });
            renderList();
        }

        tabButtons.forEach((button, index) => {
            button.addEventListener("click", () => setActiveTab(button.dataset.kaomojiTab));
            button.addEventListener("keydown", (event) => {
                if (["ArrowDown", "ArrowRight", "ArrowUp", "ArrowLeft"].includes(event.key)) {
                    event.preventDefault();
                    const direction = event.key === "ArrowDown" || event.key === "ArrowRight" ? 1 : -1;
                    const nextIndex = (index + direction + tabButtons.length) % tabButtons.length;
                    setActiveTab(tabButtons[nextIndex].dataset.kaomojiTab, true);
                }
                if (event.key === "Enter" || event.key === " ") {
                    event.preventDefault();
                    setActiveTab(button.dataset.kaomojiTab);
                }
            });
        });

